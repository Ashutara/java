// Write a java code to read byte, short, int, long, double, float, char, string, boolean variables and print all the details.

public class Datatypes {

	public static void main(String[] args) {
		
		{
			    byte  b= 2;
			    short s = 1;
			    int i = 32;
			    long l = 79797549999993l;
			    double d = 135.7545d;
			    float f = 12.2f;
			    char c = 'c';
			    boolean b1 = false;
			    
			    
			    System.out.println("The byte variable is " + b);
			    System.out.println("The short variable is " + s);
			    System.out.println("The int variable is " + i);
			    System.out.println("The long variable is " + l);
			    System.out.println("The double variable is " + d);
			    System.out.println("The float variable is " + f);
			    System.out.println("The char variable is " + c);
			    System.out.println("The boolean variable is " + b1);

			  }
			}
	}